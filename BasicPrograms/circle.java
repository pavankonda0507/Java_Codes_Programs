package BasicPrograms;
class circle {
    public static void main(String[] args) {
        double pi = 22/7.0;
        double r = 5;
        double area = pi*r*r, circum = 2*pi*r;

        System.out.println("The area of the circle:"+area);
        System.out.println("The circumference of the circle:"+circum);
    }
    
}
